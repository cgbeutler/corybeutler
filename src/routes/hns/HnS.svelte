    <h2> Starting the Clock </h2>
    <p>
        When battle or other time-sensitive moments arise, characters will be asked to “clock-in,” entering a more
        strict style of play that limits the actions they can take. When clocking in, all characters must roll to
        determine what order they take their turns in. This order will continue for the entire time that the game
        remains on the clock. Returning to off-the-clock play is up to the GM.
    </p>

    <h4> Clocking in </h4>
    <p>
        The initiative list is created in reverse order with last-place listed first. Each character, (or group of
        similar creatures,) performs a d20 roll-under Finesse check. To do this, roll a d20. If the result is above
        your finesse score, then you “miss” and get added to the initiative list. In the case of a tie, the lower
        number rolled or the smaller finesse score is added first. For characters that “hit” (rolling under or equal
        to their finesse,) repeat the check until everyone has been added to the list. (If a round goes by with all
        hits, have everyone subtract 10 from their finesse score and run the check again.)
    </p>

    <h4> On the Clock </h4>
    <p>
        Each round on the clock represents 6s of time where each character takes their turn to act and react to what’s happening.
    </p>
    <p>
        At the start of the clock, each character is given 3 actions and 1 reaction to use. These refresh at the end of their turn. If a character is surprised, they instead start with 1 action and 0 reactions for the first round.
    </p>
    <p>
        Actions can only be used during a character’s turn. Some abilities take multiple actions to perform. A character also gets at least 3 free actions. The GM may approve upwards of 5 or more free actions if the actions can be taken quickly enough and make sense for 5s rounds. Free actions include things like talking to enemies while you fight or pushing over a small object.
    </p>
    <p>
        Reactions can be used whenever its trigger arises, regardless of who’s turn it is. Any number of free reactions can be taken per round, so long as their triggers occur. Only 1 reaction or free reaction can be used per-trigger, per-character.
    </p>

    <h4>
        Ability Time and Duration
    </h4>
    <p>
	    The ability time is the amount of time needed for the ability to take effect.
    </p>
    <p>
	    The ability duration is the amount of time that the ability stays in effect.
    </p>

    <p>
        The following short-hand is used on abilities to express time and duration:
    </p>
    <table style="font-size:small;">
        <tr>
            <td>◇</td><td>Free Action</td> <td>[◆</td><td>Start of turn action</td>
        </tr>
        <tr>
            <td>◆</td><td>1 Action Cost</td> <td>◆]</td><td>End of turn action</td>
        </tr>
        <tr>
            <td>◆◆</td><td>2 Action Cost</td> <td>[1]</td><td>1 Full Turn (6s)</td>
        </tr>
        <tr>
            <td>◆◆◆</td><td>3 Action Cost</td> <td>[9]</td><td>9 Full Turn (54s)</td>
        </tr>
        <tr>
            <td>△</td><td>Free Reaction</td> <td>--</td><td>Instantaneous (or no duration)</td>
        </tr>
        <tr>
