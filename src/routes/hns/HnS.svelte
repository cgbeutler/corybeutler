    <h4>Focus</h4>
    <p>
        Some abilities require focus to perform. Breaking that focus will interrupt the action. The focus will be listed below the time and/or duration of an ability, whichever it applies to. The focus will list all the components required by the action. The focus can be on generic things like “hands” or “stance,” or the focus can list point-banks. If a point-bank is listed, that point bank is locked and cannot be used without breaking focus.
    </p>
    <p>
	    All focuses also require that the character takes no damage. Taking damage will require the character to roll (focus save > 10 or ½ dmg taken if higher)
    </p>
